let questionNumber = 0;
let question = "";
let ans1 = "";
let ans2 = "";
let ans3 = "";
let ans4 = "";
let countDown = 30;
let timerId = null;
let numOfCorrect = 0;
let numOfWrong = 0;
let numOfTimeout = 0;
let array = null;
let difficultynumber = 0;
let timerSettingSpan = 30;

function initiateDifTimer () {
    $(".difficulty").click(selectDifficulty);
    $(".timerSetting").click(selectTimer);
}

function selectDifficulty (e) {
    let difficultyLevel = e.currentTarget.innerText

    switch (difficultyLevel) {
        case "Easy":
            difficultynumber = 0;
            break;
        case "Normal":
            difficultynumber = 1;
            break;
        case "Hard":
            difficultynumber = 2;
            break
    };
    $("#difficultySpan").text(difficultyLevel);
};

function selectTimer (e) {
    let timerSpan = e.currentTarget.innerText

    switch (timerSpan) {
        case "10":
            timerSettingSpan = 10;
            countDown = timerSettingSpan;
            break;
        case "20":
            timerSettingSpan = 20;
            countDown = timerSettingSpan;
            break;
        case "30":
            timerSettingSpan = 30;
            countDown = timerSettingSpan;
            break
    };
    $("#timerSettingSpan").text(timerSpan);
};

function loadQuestion (array) {

    question = $("<div>");
    question.attr ('class', "question");
    question.html (array.question);

    ans1 = $("<div>");
    ans1.attr ('class', "answer");
    ans1.html (array.answer1);

    ans2 = $("<div>");
    ans2.attr ('class', "answer");
    ans2.html (array.answer2);

    ans3 = $("<div>");
    ans3.attr ('class', "answer");
    ans3.html (array.answer3);

    ans4 = $("<div>");
    ans4.attr ('class', "answer");
    ans4.html (array.answer4);

    let display = $("#question");
    appendQuestion (display);
    $("#timer").text("Time Remaining: " + countDown + " Seconds.");
    timerId = setInterval("countDownTimer()", 1000);
    $(".answer").click(checkAnswer);
};

function countDownTimer () {
    countDown--;
    $("#timer").remove();
    $("#timerDiv").append("<div id='timer'>Time Remaining: " + countDown + " Seconds.</div>");
    if (countDown === 0) {
        clearInterval(timerId);
        timeout ();
    };
};

function timeout () {
    timerQuestionReset();
    $("#message").append("Time Out! The Correct Answer is:", array.correct);
    $("#image").append("<img src='" + triviaQuestions[difficultynumber][questionNumber].img + "'></img>");
    setTimeout("nextQuestion()", 5000);
    countDown = timerSettingSpan;
    numOfTimeout++;
};

function checkAnswer(e) {
    let clickedDiv = e.currentTarget;

    if (clickedDiv.innerText === array.correct) {
        timerQuestionReset();
        $("#message").append("Correct!");
        $("#image").append("<img src='" + triviaQuestions[difficultynumber][questionNumber].img + "'></img>");
        clearInterval(timerId);
        setTimeout("nextQuestion()", 5000);
        countDown = timerSettingSpan;
        numOfCorrect++;

    } else {
        timerQuestionReset();
        $("#message").append("Wrong! The Correct Answer is:", array.correct);
        $("#image").append("<img src='" + triviaQuestions[difficultynumber][questionNumber].img + "'></img>");
        clearInterval(timerId);
        setTimeout("nextQuestion()", 5000);
        countDown = timerSettingSpan;
        numOfWrong++;
    };
};

function nextQuestion () {
    $("#message").remove();
    $("#messageDiv").append("<div id='message'></div>");
    $("#image").remove();
    $("#imageDiv").append("<div id='image'></div>");
    if (questionNumber === (triviaQuestions[difficultynumber].length-1)) {
        gameEnd ();
    } else {
    questionNumber++;
    array = triviaQuestions[difficultynumber][questionNumber];
    loadQuestion (array);
    };
};

function appendQuestion (display) {
    display.append(question);
    display.append(ans1);
    display.append(ans2);
    display.append(ans3);
    display.append(ans4);
};

function start () {
    array = triviaQuestions[difficultynumber][questionNumber];
    loadQuestion (array);
    $("#start").remove();
    $("#difficulty").remove();
    $("#timerSetting").remove();
};

function gameEnd () {
    $("#message").append("<div class='final'>Number of Correct: " + numOfCorrect + "</div>");
    $("#message").append("<div class='final'>Number of Wrong: " + numOfWrong + "</div>");
    $("#message").append("<div class='final'>Number of Timeout: " + numOfTimeout + "</div>");
    $("#restartDiv").append("<div class='child button2' id='restart' onClick='reset();'>Restart</div>");
};

function timerQuestionReset () {
    $("#timer").remove();
    $("#timerDiv").append("<div id='timer'></div>");
    $("#question").remove();
    $("#questionDiv").append("<div id='question'></div>");
};

function reset () {
    $("#message").remove();
    $("#messageDiv").append("<div id='message'></div>");
    $("#restart").remove();
    questionNumber = 0;
    numOfCorrect = 0;
    numOfWrong = 0;
    numOfTimeout = 0;
    difficultynumber = 0;
    timerSettingSpan = 30;
    
    $("#startDiv").append("<div class='child button2' id='start' onclick='start();'>Start</div>")
    
    $("#difficultyDiv").append("<div id='difficulty'></div>");
    $("#difficulty").append("<div class='difficulty button1'>Easy</div>");
    $("#difficulty").append("<div class='difficulty button1'>Normal</div>");
    $("#difficulty").append("<div class='difficulty button1'>Hard</div>");
    $("#difficulty").append("<div>Please Select Difficulty Level: <span id='difficultySpan'>Easy</span></div>");
    
    $("#timerSettingDiv").append("<div id='timerSetting'></div>");
    $("#timerSetting").append("<div class='timerSetting button1'>10</div>");
    $("#timerSetting").append("<div class='timerSetting button1'>20</div>");
    $("#timerSetting").append("<div class='timerSetting button1'>30</div>");
    $("#timerSetting").append("<div>Please Select Time Span For Each Questions: <span id='timerSettingSpan'>30</span> seconds</div>");
    initiateDifTimer ();
};